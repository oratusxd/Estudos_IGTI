#Criando variáveis String
rua = 'Rua do Catete, '
numero = '153, '
bairro = 'Catete, '
cidade= 'Rio de Janeiro, '
estado = ' RJ, '
cep = '22220-000'
print (rua)
# Concatenando Strings
endereco = rua + numero + bairro + cidade + estado + cep
print (endereco)
# Padronizando o texto para letra maiúscula
endereco_maiuscula = endereco.upper()
print (endereco_maiuscula)
#Padronizando o texto para letra minúscula
endereco_minuscula = endereco.lower()
print(endereco_minuscula)